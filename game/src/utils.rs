use std::env;

////////////////////////////////////////////////////////////////////////////////////////////////////
// Constants
////////////////////////////////////////////////////////////////////////////////////////////////////

pub static VERSION: &str = env!("CARGO_PKG_VERSION");

////////////////////////////////////////////////////////////////////////////////////////////////////
// Enums
////////////////////////////////////////////////////////////////////////////////////////////////////

#[derive(Clone, Copy, Debug)]
pub enum ExitCode {
    Ok = 0,
    OutOfMemory,
    OutOfVideoMemory,
}

impl ExitCode {
    pub fn as_int(&self) -> i32 {
        *self as i32
    }
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// Testing
////////////////////////////////////////////////////////////////////////////////////////////////////

/// Buffers should be 8 byte aligned
#[macro_export]
macro_rules! test_buffer_align {
    ($test_type:ty) => {
        #[cfg(test)]
        #[test]
        fn test_alignment() {
            assert_eq!(core::mem::size_of::<$test_type>() % 8, 0);
        }
    };
}
